package com.yomuka.yomuka.main;

public class Comments {
	private int commentsaid;
	private int boardaid;
	private String memberid;
	private String content;
	private String date;
		
	public int getCommentsaid() {
		return commentsaid;
	}
	public void setCommentsaid(int commentsaid) {
		this.commentsaid = commentsaid;
	}
	public int getBoardaid() {
		return boardaid;
	}
	public void setBoardaid(int boardaid) {
		this.boardaid = boardaid;
	}
	public String getMemberid() {
		return memberid;
	}
	public void setMemberid(String memberid) {
		this.memberid = memberid;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
